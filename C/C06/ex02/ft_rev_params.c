#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;

	i = argc - 1;
	if (argc < 1)
		return (0);
	while (0 < i)
	{
		while (*argv[i])
		{
			write(1, argv[i], 1);
			argv[i]++;
		}
		i--;
		write(1, "\n", 1);
	}
	return (0);
}
