char	*ft_strchr(const char *s, int c)
{
	int		n;

	n = 0;
	while (s[n])
	{
		if (s[n] == c)
			return ((char *)&s[n]);
		n++;
	}
	if (c == '\0')
		return ((char *)&s[n]);
	return (0);
}
