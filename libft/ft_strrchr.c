char	*ft_strrchr(const char *s, int c)
{
	char	first;
	int		n;

	first = c;
	n = 0;
	while (s[n])
		n++;
	while (-1 < n)
	{
		if (s[n] == c)
			return ((char *)&s[n]);
		n--;
	}
	if (c == '\0')
		return ((char *)&s[n]);
	return (0);
}
